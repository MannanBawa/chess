package generalClasses.Position;

/**
 * Interface for the Position class.
 */
public interface IPosition {

  /**
   * Getter for position x value.
   */
  int getX();

  /**
   * Getter for position y value.
   */
  int getY();

  /**
   * To string method that returns Position as a formatted string.
   */
  String toString();
}
